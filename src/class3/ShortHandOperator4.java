package class3;

public class ShortHandOperator4 {
    public static void main(String[] args) {
        int number=10;
        number+=10;// whatever number is stored in number variable add 10 to it
        //same as number=number+10
        System.out.println(number);

        int number2=20;
        number2-=10;
        System.out.println(number2);

        int number3=100;
        number3/=10;
        System.out.println(number3);
    }
}
